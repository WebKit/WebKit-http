ViewBackendBCMRPi::ViewBackendBCMRPi()
    : m_elementHandle(DISPMANX_NO_HANDLE)
    , m_width(0)
    , m_height(0)
{
    bcm_host_init();
    m_displayHandle = vc_dispmanx_display_open(0);
    graphics_get_display_size(DISPMANX_ID_HDMI, &m_width, &m_height);
}

ViewBackendBCMRPi::~ViewBackendBCMRPi()
{
    LibinputServer::singleton().setClient(nullptr);
}

void ViewBackendBCMRPi::setClient(Client* client)
{
    m_client = client;
    if (m_client)
        m_client->setSize(m_width, m_height);
}

uint32_t ViewBackendBCMRPi::createBCMElement(int32_t width, int32_t height)
{
    static VC_DISPMANX_ALPHA_T alpha = {
        static_cast<DISPMANX_FLAGS_ALPHA_T>(DISPMANX_FLAGS_ALPHA_FIXED_ALL_PIXELS),
        255, 0
    };

    if (m_elementHandle != DISPMANX_NO_HANDLE)
        return 0;

    uint32_t clampedWidth = std::max(0, width);
    uint32_t clampedHeight = std::max(0, height);
    if (m_width != clampedWidth || m_height != clampedHeight)
        fprintf(stderr, "ViewBackendBCMRPi: mismatch in buffer parameters during creation\n");

    DISPMANX_UPDATE_HANDLE_T updateHandle = vc_dispmanx_update_start(0);

    VC_RECT_T srcRect, destRect;
    vc_dispmanx_rect_set(&srcRect, 0, 0, m_width << 16, m_height << 16);
    vc_dispmanx_rect_set(&destRect, 0, 0, m_width, m_height);

    m_elementHandle = vc_dispmanx_element_add(updateHandle, m_displayHandle, 0,
        &destRect, DISPMANX_NO_HANDLE, &srcRect, DISPMANX_PROTECTION_NONE,
        &alpha, nullptr, DISPMANX_NO_ROTATE);

    vc_dispmanx_update_submit_sync(updateHandle);
    return m_elementHandle;
}

void ViewBackendBCMRPi::commitBCMBuffer(uint32_t elementHandle, uint32_t width, uint32_t height)
{
    DISPMANX_UPDATE_HANDLE_T updateHandle = vc_dispmanx_update_start(0);

    if (elementHandle != m_elementHandle || m_width != width || m_height != height)
        fprintf(stderr, "ViewBackendBCMRPi: mismatch in buffer parameters during update\n");

    VC_RECT_T srcRect, destRect;
    vc_dispmanx_rect_set(&srcRect, 0, 0, m_width << 16, m_height << 16);
    vc_dispmanx_rect_set(&destRect, 0, 0, m_width, m_height);

    vc_dispmanx_element_change_attributes(updateHandle, m_elementHandle, 1 << 3 | 1 << 2, 0, 0, &destRect, &srcRect, 0, DISPMANX_NO_ROTATE);

    vc_dispmanx_update_submit_sync(updateHandle);

    if (m_client)
        m_client->frameComplete();
}

void ViewBackendBCMRPi::setInputClient(Input::Client* client)
{
    if (std::getenv("WPE_BCMRPI_CURSOR")) {
        m_cursor.reset(new Cursor(client, m_displayHandle, m_width, m_height));
        client = m_cursor.get();
    }
    LibinputServer::singleton().setClient(client);
    LibinputServer::singleton().setPointerBounds(m_width, m_height);
}

ViewBackendBCMRPi::Cursor::Cursor(Input::Client* targetClient, DISPMANX_DISPLAY_HANDLE_T displayHandle, uint32_t displayWidth, uint32_t displayHeight)
    : m_targetClient(targetClient)
    , m_position(0, 0)
    , m_displaySize(displayWidth, displayHeight)
{
    static VC_DISPMANX_ALPHA_T alpha = {
        static_cast<DISPMANX_FLAGS_ALPHA_T>(DISPMANX_FLAGS_ALPHA_FROM_SOURCE),
        255, 0
    };

    DISPMANX_UPDATE_HANDLE_T updateHandle = vc_dispmanx_update_start(0);

    uint32_t imagePtr;
    VC_RECT_T rect;
    vc_dispmanx_rect_set(&rect, 0, 0, Data::width, Data::height);
    DISPMANX_RESOURCE_HANDLE_T pointerResource = vc_dispmanx_resource_create(VC_IMAGE_RGBA32, Data::width, Data::height, &imagePtr);
    vc_dispmanx_resource_write_data(pointerResource, VC_IMAGE_RGBA32, Data::width * 4, Data::data, &rect);

    VC_RECT_T srcRect, destRect;
    vc_dispmanx_rect_set(&srcRect, 0, 0, Data::width << 16, Data::height << 16);
    vc_dispmanx_rect_set(&destRect, m_position.first, m_position.second, cursorWidth, cursorHeight);

    m_cursorHandle = vc_dispmanx_element_add(updateHandle, displayHandle, 10,
        &destRect, pointerResource, &srcRect, DISPMANX_PROTECTION_NONE, &alpha,
        nullptr, DISPMANX_NO_ROTATE);

    vc_dispmanx_resource_delete(pointerResource);

    vc_dispmanx_update_submit_sync(updateHandle);
}

ViewBackendBCMRPi::Cursor::~Cursor()
{
    DISPMANX_UPDATE_HANDLE_T updateHandle = vc_dispmanx_update_start(0);
    vc_dispmanx_element_remove(updateHandle, m_cursorHandle);
    vc_dispmanx_update_submit_sync(updateHandle);
}

void ViewBackendBCMRPi::Cursor::handleKeyboardEvent(Input::KeyboardEvent&& event)
{
    m_targetClient->handleKeyboardEvent(std::move(event));
}

void ViewBackendBCMRPi::Cursor::handlePointerEvent(Input::PointerEvent&& event)
{
    m_targetClient->handlePointerEvent(std::move(event));

    DISPMANX_UPDATE_HANDLE_T updateHandle = vc_dispmanx_update_start(0);

    VC_RECT_T destRect;
    vc_dispmanx_rect_set(&destRect, event.x, event.y,
        std::min<uint32_t>(cursorWidth, std::max<uint32_t>(0, m_displaySize.first - event.x)),
        std::min<uint32_t>(cursorHeight, std::max<uint32_t>(0, m_displaySize.second - event.y)));

    vc_dispmanx_element_change_attributes(updateHandle, m_cursorHandle, 1 << 2,
        0, 0, &destRect, nullptr, DISPMANX_NO_HANDLE, DISPMANX_NO_ROTATE);

    vc_dispmanx_update_submit_sync(updateHandle);
}

void ViewBackendBCMRPi::Cursor::handleAxisEvent(Input::AxisEvent&& event)
{
    m_targetClient->handleAxisEvent(std::move(event));
}

void ViewBackendBCMRPi::Cursor::handleTouchEvent(Input::TouchEvent&& event)
{
    m_targetClient->handleTouchEvent(std::move(event));
}

